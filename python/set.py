#set elements addition and removing
s={1,2,3}
s.add(5)
print(s)
s.remove(2)
print(s)
s.discard(1)
print(s)
s.pop()
print(s)
s.clear()
print(s)
#set operations
print("set")
s1={1,2,3}
s2={3,4,5}
s=s1.union(s2)
print(s)
s=s1.difference(s2)
print(s)
s=s1.intersection(s2)
print(s)
s=s1.symmetric_difference(s2)
print(s)
s={1,2,3,4}
j={2,4}
print(j.issubset(s))
print(s.issuperset(j))
print(j.isdisjoint(s))
